# Two tuples representing dates (year, month, day)
date1 = (2023, 4, 15)
date2 = (2023, 4, 20)

# Compare the dates
if date1 < date2:
    print("The first date is earlier than the second date.")
elif date1 > date2:
    print("The first date is later than the second date.")
else:
    print("The first date is the same as the second date.")

