# Tuple containing the names of months
months = ("January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December")

# Slice the tuple to get the first half of the months
first_half_months = months[:len(months)//2]

# Print the new tuple containing the first half of the months
print(first_half_months)

